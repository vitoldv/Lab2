public class StrEdit {

    public String toLow(String str) {
        return str.toLowerCase();
    }

    public String toUp(String str) {
        return str.toUpperCase();
    }

    public int size(String str) {
        return str.length();
    }

    public static String merge(String str1, String str2) {
        return (str1 + str2);
    }
}